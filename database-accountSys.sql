CREATE TABLE Clients(
id INT AUTO_INCREMENT PRIMARY KEY NOT NULL,
firstname VARCHAR(50) NOT NULL,
lastname VARCHAR(50) NOT NULL,
gender CHAR(10) NOT NULL,
username VARCHAR(50) NOT NULL,
password VARCHAR(50) NOT NULL,
UNIQUE (username,password)
);

CREATE TABLE Accounts(
	accountID INT AUTO_INCREMENT NOT NULL PRIMARY KEY,
    clientUsername VARCHAR(50) NOT NULL,
	clientPassword VARCHAR(50) NOT NULL,
    FOREIGN KEY (accountID) REFERENCES Clients(id),
    FOREIGN KEY (clientUsername,clientPassword) REFERENCES Clients(username,password)
    );
CREATE TABLE bankAccounts(
	accountIDStored INT AUTO_INCREMENT NOT NULL  PRIMARY KEY,
    balance INT NOT NULL,
	pinNumber VARCHAR(50) NOT NULL,
    bankClients VARCHAR(50) NOT NULL,
    FOREIGN KEY (accountIDStored) REFERENCES Accounts(accountID),
     FOREIGN KEY (bankClients) REFERENCES Accounts(clientUsername)
    );

